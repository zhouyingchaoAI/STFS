# 打印输出HOLIDAYS的type枚举，输出为 {原始type字符串: int枚举}
def get_holiday_type_enum_map(holidays):
    type_list = sorted(set(v['type'] for v in holidays.values()))
    return {t: i for i, t in enumerate(type_list)}

